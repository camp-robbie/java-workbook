package main.java.solutions.workbook1.controlflow;

import java.util.Scanner;

public class Q6 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        int count = 0; // 자릿수 합이 5의 배수인 수의 개수

        // 1부터 n까지 반복
        for (int i = 1; i <= n; i++) {
            int num = i;
            int digitSum = 0; // 현재 수의 자릿수 합

            // 현재 수의 각 자릿수를 더함
            while (num > 0) {
                digitSum += num % 10; // 마지막 자릿수 추출하여 더함
                num /= 10;            // 마지막 자릿수 제거
            }

            // 자릿수 합이 5의 배수인지 확인
            if (digitSum % 5 == 0) {
                count++;
            }
        }

        System.out.println(count);
        sc.close();
    }
}

/**
 * **코드 설명:**
 * 1. **외부 for문**: 1부터 N까지 각 수를 검사
 * 2. **내부 while문**: 현재 수 `i`의 각 자릿수를 추출하여 합 계산
 *     - `num % 10`: 마지막 자릿수 추출
 *     - `num /= 10`: 마지막 자릿수 제거
 *     - `digitSum += 추출한 자릿수`: 자릿수 합에 더함
 *
 * 3. **조건 확인**: `digitSum % 5 == 0`이면 5의 배수이므로 개수 증가
 * 4. **결과 출력**: 조건을 만족하는 수의 총 개수 출력
 *
 * **예시 실행 (N=20):**
 * 각 수의 자릿수 합을 계산해보면:
 * - 1: 1 (5의 배수 아님)
 * - 2: 2 (5의 배수 아님)
 * - 3: 3 (5의 배수 아님)
 * - 4: 4 (5의 배수 아님)
 * - **5: 5** ✓ (5의 배수)
 * - 6: 6 (5의 배수 아님)
 * - 7: 7 (5의 배수 아님)
 * - 8: 8 (5의 배수 아님)
 * - 9: 9 (5의 배수 아님)
 * - **10: 1+0 = 1** (5의 배수 아님)
 * - 11: 1+1 = 2 (5의 배수 아님)
 * - 12: 1+2 = 3 (5의 배수 아님)
 * - 13: 1+3 = 4 (5의 배수 아님)
 * - **14: 1+4 = 5** ✓ (5의 배수)
 * - 15: 1+5 = 6 (5의 배수 아님)
 * - 16: 1+6 = 7 (5의 배수 아님)
 * - 17: 1+7 = 8 (5의 배수 아님)
 * - 18: 1+8 = 9 (5의 배수 아님)
 * - 19: 1+9 = 10 ✓ (5의 배수)
 * - 20: 2+0 = 2 (5의 배수 아님)
 *
 * 조건을 만족하는 수: **5, 14, 19** (총 3개)
 */